"""Render the GPU benchmark figure from one or more bench_*.json files.

Reads JSON produced by ``python -m cranpm.gpu.bench`` (see
``cranpm.gpu.bench``) and writes:

* ``fig_gpu_benchmarks.{pdf,png}``     — throughput (maps/s) + latency (ms) +
                                          peak memory (MiB), grouped by precision.
* ``table_gpu_benchmarks.tex``         — LaTeX table for the paper.

Usage::

    python fig_gpu_benchmarks.py \\
        --inputs paper/figures/bench_rocm.json paper/figures/bench_cuda.json \\
        --output-dir paper/figures
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

import matplotlib.pyplot as plt
import numpy as np

from cranpm_plots import apply_paper_style, save_figure

PRECISION_ORDER = ["fp32", "bf16", "fp16"]
PRECISION_COLORS = {
    "fp32": "#7f7f7f",
    "bf16": "#1f77b4",
    "fp16": "#ff7f0e",
}


def load_runs(paths):
    runs = []
    for p in paths:
        with open(p) as fh:
            blob = json.load(fh)
        label = blob.get("device_info", {}).get("device_name", blob.get("backend", "GPU"))
        if blob.get("torch_hip"):
            label += " (ROCm)"
        elif blob.get("torch_cuda"):
            label += f" (CUDA {blob['torch_cuda']})"
        runs.append((label, blob["results"]))
    return runs


def panel(ax, runs, key, ylabel, title, log=False):
    for run_idx, (label, results) in enumerate(runs):
        # group by precision
        for prec in PRECISION_ORDER:
            rows = [r for r in results if r.get("precision") == prec and key in r]
            if not rows:
                continue
            bs = sorted({r["batch_size"] for r in rows})
            ys = [r[key] for r in sorted(rows, key=lambda r: r["batch_size"])]
            linestyle = "-" if run_idx == 0 else "--"
            ax.plot(
                bs, ys,
                marker="o", markersize=4,
                color=PRECISION_COLORS.get(prec, "#444"),
                linestyle=linestyle,
                label=f"{label}, {prec}" if run_idx == 0 and key == "maps_per_sec"
                       else (f"{label}" if key == "maps_per_sec" and run_idx > 0
                             else None),
            )
    ax.set_xlabel("Batch size (tiles per forward)")
    ax.set_ylabel(ylabel)
    ax.set_title(title, pad=4)
    ax.grid(linestyle="--", alpha=0.4)
    ax.set_axisbelow(True)
    if log:
        ax.set_yscale("log")


def render_bars(runs, output_dir: Path):
    apply_paper_style()
    fig, axes = plt.subplots(1, 3, figsize=(13, 4.0))

    panel(axes[0], runs, "maps_per_sec",
          "Throughput (maps / s)", "Throughput")
    panel(axes[1], runs, "per_call_ms",
          "Latency (ms / forward)", "Latency")
    panel(axes[2], runs, "peak_mem_mib",
          "Peak GPU memory (MiB)", "Peak memory")

    # Build a single legend from a custom proxy.
    handles = []
    for prec in PRECISION_ORDER:
        if any(any(r.get("precision") == prec for r in res) for _, res in runs):
            handles.append(plt.Line2D([0], [0], color=PRECISION_COLORS[prec],
                                       marker="o", label=prec))
    if len(runs) > 1:
        for i, (label, _) in enumerate(runs):
            handles.append(plt.Line2D([0], [0], color="#444",
                                       linestyle=("-" if i == 0 else "--"),
                                       label=label))
    axes[0].legend(handles=handles, loc="best", fontsize=8)

    fig.suptitle("CRAN-PM single-GPU inference benchmarks", y=0.99)
    fig.tight_layout(rect=[0, 0, 1, 0.95])
    save_figure(fig, "fig_gpu_benchmarks")
    plt.close(fig)


def render_latex_table(runs, output_dir: Path):
    out = output_dir / "table_gpu_benchmarks.tex"
    with open(out, "w") as fh:
        fh.write("% Auto-generated by fig_gpu_benchmarks.py\n")
        fh.write("\\begin{tabular}{l l r r r r}\n")
        fh.write("\\toprule\n")
        fh.write(
            "Device & Precision & Batch & Latency (ms) & "
            "Throughput (maps/s) & Peak (MiB) \\\\\n"
        )
        fh.write("\\midrule\n")
        for label, results in runs:
            for r in results:
                if "error" in r:
                    continue
                fh.write(
                    f"{label} & {r['precision']} & {r['batch_size']} & "
                    f"{r['per_call_ms']:.1f} & {r['maps_per_sec']:.3f} & "
                    f"{r['peak_mem_mib']:.0f} \\\\\n"
                )
        fh.write("\\bottomrule\n")
        fh.write("\\end{tabular}\n")
    print(f"Wrote {out}")


def main(argv=None):
    parser = argparse.ArgumentParser()
    parser.add_argument("--inputs", nargs="+", type=Path, required=True)
    parser.add_argument("--output-dir", type=Path, required=True)
    args = parser.parse_args(argv)
    args.output_dir.mkdir(parents=True, exist_ok=True)
    runs = load_runs(args.inputs)
    render_bars(runs, args.output_dir)
    render_latex_table(runs, args.output_dir)


if __name__ == "__main__":
    sys.exit(main())
